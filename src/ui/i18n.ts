const CATALOGS: Record<Lang, Catalog> = {
  en: EN, es: ES, zh: ZH, hi: HI, ar: AR, pt: PT, bn: BN, ru: RU, ja: JA, de: DE,
  fr: FR, ko: KO, it: IT, tr: TR, vi: VI, id: ID, pl: PL, nl: NL, th: TH, uk: UK,
};

let current: Lang = 'en';

export function setLang(lang: Lang) {
  current = CATALOGS[lang] ? lang : 'en';
}

export function getLang(): Lang {
  return current;
}

export function t(key: string, params?: Record<string, string | number>): string {
  let s = CATALOGS[current][key] ?? EN[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    }
  }
  return s;
}

/** Returns the sorted list of keys defined by the English catalog (authoritative). */
export function enKeys(): string[] {
  return Object.keys(EN).sort();
}

/** Returns the sorted list of keys defined by the given language's catalog. */
export function keysFor(lang: Lang): string[] {
  return Object.keys(CATALOGS[lang] ?? {}).sort();
}

/** Returns EN keys that are missing from the given language catalog. */
export function missingKeys(lang: Lang): string[] {
  const target = CATALOGS[lang] ?? {};
  return enKeys().filter((k) => !(k in target));
}
